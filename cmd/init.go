var initCmd = &cobra.Command{
	Use:   "init",
	Short: "Create virt-go network",
	Run: func(cmd *cobra.Command, args []string) {
		conn, err := libvirt.NewConnect("qemu:///system")
		if err != nil {
			log.Fatal(err)
		}
		defer conn.Close()

		fmt.Println("■  Create network XML file.")
		netXML := createNetwork()
		net, err := conn.NetworkDefineXML(netXML)
		if err != nil {
			log.Fatal(err)
		}
		fmt.Println("■  Set 'virt-go-net' to autostart.")
		err = net.SetAutostart(true)
		if err != nil {
			log.Fatal(err)
		}
		fmt.Println("■  " + colorGreen + "Start 'virt-go-net'." + colorReset)
		err = net.Create()
		if err != nil {
			log.Fatal(err)
		}
	},
}

